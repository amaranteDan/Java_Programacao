
public class exercicioIf {

	public static void main(String[] args) {
		int cedula = 10;
		int moeda = 5;
		int carro = 5000;
		int onibus = 50000;
		
		
		if(cedula > moeda){
			System.out.println(cedula);
		}
		if (carro < onibus){
			System.out.println(carro);
		}else{
			System.out.println(onibus);
		}
	}

}
